#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	sym;

	sym = c;
	if ((sym >= 65 && sym <= 90) || (sym >= 97 && sym <= 122))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
